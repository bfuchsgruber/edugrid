/** Get a Load voltage from data
 * @return Voltage in V
 */
float edugrid_simulation::getVoltageLoad_simulation()
{
    /* U_out = duty_cycle * U_in */
    return edugrid_pwm_control::getPWM_normalized() * getVoltagePV_simulation();
}

/** Get a PV current from data
 * @return Current in A
 */
float edugrid_simulation::getCurrentPV_simulation()
{
    return i_in_sim[array_counter];
    // return (-5e-6*getVoltagePV_simulation());
}

/** Get a Load current from data
 * @return Current in A
 */
float edugrid_simulation::getCurrentLoad_simulation()
{
    float P_out_sim = edugrid_measurement::P_in * (float)ETA_BUCK / 100.0;
    return (P_out_sim / edugrid_measurement::V_out);
}

/** Get Simulation state from flash --> done after starting
 */
void edugrid_simulation::readSimulationStateFromFlash()
{
    if (edugrid_filesystem::config_simulation_state == "1")
    {
        activateSimulation();
    }
    else
    {
        deactivateSimulation();
    }
}

/** Get Simulation state in a String (ON / OFF)
 */
String edugrid_simulation::getSimulationState_str()
{
    if (simulation_state)
    {
        return "ON";
    }
    else
    {
        return "OFF";
    }
}

/** Get Simulation state in a bool
 */
bool edugrid_simulation::getSimulationState()
{
    return simulation_state;
}

/** Activate Simulation */
void edugrid_simulation::activateSimulation()
{
    simulation_state = true;
    edugrid_filesystem::writeContent_str(CONFIG_FILEPATH_SIMULATION, "1");
}

/** Deactivate Simulation */
void edugrid_simulation::deactivateSimulation()
{
    simulation_state = false;
    edugrid_filesystem::writeContent_str(CONFIG_FILEPATH_SIMULATION, "0");
}

/** Toggle Simulation State */
void edugrid_simulation::toggleSimulation()
{
    if (getSimulationState())
    {
        deactivateSimulation();
    }
    else
    {
        activateSimulation();
    }
}